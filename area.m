%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Function to compute the nozzle area               
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function aa = area(x)
  if(x > 0)
    aa = 0.536572-0.198086*exp(-log(2.)*(x/0.6)^2);
  else
    aa = 1.0 - 0.661514*exp(-log(2.)*(x/0.6)^2);
  end 
end